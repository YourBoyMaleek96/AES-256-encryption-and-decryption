def key_expansion(key):
    """
    Expands the key for AES-256.
    """
    # Initialize the key schedule array
    key_schedule = np.zeros((4, Nb * (Nr + 1)), dtype=np.uint8)
    
    # First Nk words are from the key
    key_schedule[:, :Nk] = key.reshape(4, -1, order='F')
    
    # Generate the rest of the key schedule
    for i in range(Nk, Nb * (Nr + 1)):
        temp = key_schedule[:, i-1].copy()
        
        if i % Nk == 0:
            # RotWord
            temp = np.roll(temp, -1)
            # SubWord
            temp = sbox[temp]
            # XOR with round constant
            temp[0] ^= rcon[i//Nk - 1]
        elif Nk > 6 and i % Nk == 4:
            # Additional SubWord for AES-256
            temp = sbox[temp]
        
        key_schedule[:, i] = key_schedule[:, i-Nk] ^ temp
    
    return key_schedule

def add_round_key(state, round_key):
    return state ^ round_key

def sub_bytes(state):
    return sbox[state]

def inv_sub_bytes(state):
    return inv_sbox[state]

def shift_rows(state):
    """
    ShiftRows cyclically shifts the rows of the state over different offsets.
    """
    # Make sure we're working with a copy
    state = state.copy()
    
    # Shift row 1 by 1 position to the left
    state[1] = np.roll(state[1], -1)
    
    # Shift row 2 by 2 positions to the left
    state[2] = np.roll(state[2], -2)
    
    # Shift row 3 by 3 positions to the left
    state[3] = np.roll(state[3], -3)
    
    return state

def inv_shift_rows(state):
    """
    Inverse of ShiftRows operation.
    """
    # Make sure we're working with a copy
    state = state.copy()
    
    # Shift row 1 by 1 position to the right
    state[1] = np.roll(state[1], 1)
    
    # Shift row 2 by 2 positions to the right
    state[2] = np.roll(state[2], 2)
    
    # Shift row 3 by 3 positions to the right
    state[3] = np.roll(state[3], 3)
    
    return state

def mix_columns(state):
    """
    MixColumns transformation
    """
    for i in range(4):
        col = state[:, i]
        state[:, i] = [
            galois_mult(col[0], 2) ^ galois_mult(col[1], 3) ^ col[2] ^ col[3],
            col[0] ^ galois_mult(col[1], 2) ^ galois_mult(col[2], 3) ^ col[3],
            col[0] ^ col[1] ^ galois_mult(col[2], 2) ^ galois_mult(col[3], 3),
            galois_mult(col[0], 3) ^ col[1] ^ col[2] ^ galois_mult(col[3], 2)
        ]
    return state

def inv_mix_columns(state):
    """
    Inverse MixColumns transformation
    """
    for i in range(4):
        col = state[:, i]
        state[:, i] = [
            galois_mult(col[0], 0x0e) ^ galois_mult(col[1], 0x0b) ^ galois_mult(col[2], 0x0d) ^ galois_mult(col[3], 0x09),
            galois_mult(col[0], 0x09) ^ galois_mult(col[1], 0x0e) ^ galois_mult(col[2], 0x0b) ^ galois_mult(col[3], 0x0d),
            galois_mult(col[0], 0x0d) ^ galois_mult(col[1], 0x09) ^ galois_mult(col[2], 0x0e) ^ galois_mult(col[3], 0x0b),
            galois_mult(col[0], 0x0b) ^ galois_mult(col[1], 0x0d) ^ galois_mult(col[2], 0x09) ^ galois_mult(col[3], 0x0e)
        ]
    return state

def galois_mult(a, b):
    """
    Galois Field multiplication
    """
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        hi_bit_set = a & 0x80
        a <<= 1
        if hi_bit_set:
            a ^= 0x1b
        b >>= 1
    return p & 0xff

def aes_encrypt(plaintext, key):
    # Create the state array in column-major order
    state = np.array(plaintext, dtype=np.uint8).reshape(4, 4, order='F')
    
    key_schedule = key_expansion(key)
    
    # Initial round
    print(f"round[ {0}].input {''.join([hex(b)[2:].zfill(2) for b in state.T.flatten()])}")
    print(f"round[ {0}].k_sch {''.join([hex(b)[2:].zfill(2) for b in key_schedule[:, :4].T.flatten()])}")
    
    state = add_round_key(state, key_schedule[:, :4])

    # Main rounds
    for round in range(1, Nr):
        print(f"round[ {round}].start {''.join([hex(b)[2:].zfill(2) for b in state.T.flatten()])}")
        state = sub_bytes(state)
        print(f"round[ {round}].s_box {''.join([hex(b)[2:].zfill(2) for b in state.T.flatten()])}")
        state = shift_rows(state)
        print(f"round[ {round}].s_row {''.join([hex(b)[2:].zfill(2) for b in state.T.flatten()])}")
        state = mix_columns(state)
        print(f"round[ {round}].m_col {''.join([hex(b)[2:].zfill(2) for b in state.T.flatten()])}")
        state = add_round_key(state, key_schedule[:, 4*round:4*(round+1)])
        print(f"round[ {round}].k_sch {''.join([hex(b)[2:].zfill(2) for b in key_schedule[:, 4*round:4*(round+1)].T.flatten()])}")

    # Final round
    print(f"round[{Nr}].start {''.join([hex(b)[2:].zfill(2) for b in state.T.flatten()])}")
    state = sub_bytes(state)
    print(f"round[{Nr}].s_box {''.join([hex(b)[2:].zfill(2) for b in state.T.flatten()])}")
    state = shift_rows(state)
    print(f"round[{Nr}].s_row {''.join([hex(b)[2:].zfill(2) for b in state.T.flatten()])}")
    state = add_round_key(state, key_schedule[:, 4*Nr:4*(Nr+1)])
    print(f"round[{Nr}].k_sch {''.join([hex(b)[2:].zfill(2) for b in key_schedule[:, 4*Nr:4*(Nr+1)].T.flatten()])}")

    return state.T.flatten()

